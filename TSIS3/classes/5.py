class Account():
    def __init__(self, owner, balance):
        self.owner = owner
        self.balance = balance

    def deposit(money_d):
        new_balance=balance+money_d
        return new_balance

    def withdraw(self, money_w):
        money_w<=balance
        reduced_balance=balance-money_w
        return reduced_balance
a = int(input())
s = Account(a)
print(s.deposit())
print(s.withdraw())
